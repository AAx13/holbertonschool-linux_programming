/**
 * add - returns sum of two numbers.
 * @a: An integer.
 * @b: An integer.
 *
 * Return: Sum (int).
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - returns value of a subtracted by b.
 * @a: An integer.
 * @b: An integer.
 *
 * Return: Value (int).
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - returns value of a multiplied by b.
 * @a: An integer.
 * @b: An integer.
 *
 * Return: Value (int).
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - returns value of a divided by b.
 * @a: An integer.
 * @b: An integer.
 *
 * Return: Value (int).
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - returns the remainder of a divided by b.
 * @a: An integer.
 * @b: An integer.
 *
 * Return: Remainder (int).
 */
int mod(int a, int b)
{
	return (a % b);
}
